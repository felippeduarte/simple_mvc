Algumas considerações:

<ul>
    <li>Foi desenvolvido um pequeno Framework para desenvolver o projeto. A ideia era evitar um trabalho simples só com POST e GET e fazer algo com padrões de projeto como FrontController, Singleton, além de usar MVC, Dependency Injection, Interfaces, etc. </li>
    <li>A parte visual foi propositadamente deixada de lado. Como o cargo é para desenvolvedor back-end, fiz o mínimo necessário no front-end para montar o CRUD, de forma estruturada e organizada.</li>
    <li>Dado o alto volume de dados previsto (50 mil/mês), seria interessante usar formas de busca, filtros, ordenação e paginação, quando necessário, porém não foi dado como requisito.</li>
    <li>Foi realizada uma carga inicial de clientes e contratos, com dados aleatórios.</li>
    <li>Para cadastrar Clientes ou Contratos, basta clicar acima e depois em "Cadastrar Novo"</li>
    <li>Para editar Clientes ou Contratos, basta clicar acima em cliente/contrato e depois no ícone do lápis na linha da tabela</li>
    <li>Para remover Clientes ou Contratos, basta clicar acima em cliente/contrato e depois no ícone da lixeira na linha da tabela</li>
    <li>Para visualizar contratos de clientes, basta clicar no nome ou id do cliente na tabela</li>
    <li>Para visualizar o cliente do contrato, basta clicar no nome ou id do contrato na tabela</li>
    
    <li>Para manter o sigilo do código fonte não quis colocar em repositórios como GitHub ou Bitbucket. Adicionei o código fonte zipado <a href="teste02.zip">neste link</a></li>
    
    <li>Espero que gostem :)</li>
</ul>